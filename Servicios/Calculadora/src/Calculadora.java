public class Calculadora {

	public float sumar(float s1, float s2) {
		return (s1 + s2);
	}

	public float restar(float min, float sus) {
		return (min - sus);
	}

	public float multiplicar(float m1, float m2) {
		return (m1 * m2);
	}

	public float dividir(float num, float den) {
		return (num /den);
	}
	
}